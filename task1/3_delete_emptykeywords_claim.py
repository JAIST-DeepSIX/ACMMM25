import json

with open("output/2_to_keyphrase.json", "r", encoding="utf-8") as f:
    data = json.load(f)

for item in data:
    filtered_response = []
    for segment in item["response"]:
        # Filter claims in this segment
        new_claims = [
            claim for claim in segment["claims"]
            if claim.get("keywords")  # truthy only if non‐empty list
        ]
        # Only keep the segment if at least one claim remains
        if new_claims:
            filtered_response.append({
                "segment": segment["segment"],
                "claims": new_claims
            })
    item["response"] = filtered_response

# 3. Write filtered data back out
with open("output/3_data_onlyclaim_wkeyphrase.json", "w", encoding="utf-8") as f:
    json.dump(data, f, ensure_ascii=False, indent=2)
