import json

if __name__ == "__main__":
    # Load the JSON file
    with open("output/5_pre_final.json", "r", encoding='utf-8') as file:
        data = json.load(file)
    
    # Iterate through each sample in the data
    cnt = 0
    for sample in data:
        if sample['final_shot'].find("Skipped sample with no multiple predictions") != -1:
            # If the final_shot contains "Skipped sample with no multiple predictions", skip this sample
            continue
        else: 
            st_pos = sample['final_shot'].find("\"Answer\":")
            final_ans = sample['final_shot'][st_pos:]
            if sample["correct_choice"] not in final_ans:
                print(f"Sample ID: {sample['image_id']}")
                cnt +=1 

            if "B" in final_ans:
                sample['prediction'] = ["B"]
            elif "C" in final_ans:
                sample['prediction'] = ["C"]
            elif "D" in final_ans:
                sample['prediction'] = ["D"]
                
            if sample["correct_choice"] in final_ans:
                sample['correct'] = True      
                
    print(cnt)
    
    for sample in data:
        del sample["final_shot"]
        del sample["correct"]
    # Save the modified data back to a new JSON file
    with open("output/6_final_final.json", "w", encoding='utf-8') as file:
        json.dump(data, file, ensure_ascii=False, indent=4)